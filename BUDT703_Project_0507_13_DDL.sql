--SQL Create Tables

--State Table
CREATE TABLE [Amtrak.State] (
 stateCode CHAR(2) NOT NULL,
 stateName VARCHAR(40) NOT NULL
 CONSTRAINT pk_State_stateCode PRIMARY KEY (stateCode)
);

--Station Table 
CREATE TABLE [Amtrak.Station] (
 stationCode CHAR(3) NOT NULL,
 stationName VARCHAR(100) NOT NULL,
 stateCode CHAR(2)
 CONSTRAINT pk_Station_stationCode PRIMARY KEY (stationCode),
 CONSTRAINT fk_Station_stateCode FOREIGN KEY (stateCode)
 REFERENCES [Amtrak.State](stateCode)
  ON DELETE CASCADE ON UPDATE CASCADE
);

--StationMetrics Table
CREATE TABLE [Amtrak.StationMetrics] (
 stationCode CHAR(3) NOT NULL,
 stationMetric VARCHAR(30) NOT NULL,
 stationMetricYear INT NOT NULL,
 stationMetricValue FLOAT,
 CONSTRAINT pk_StationMetrics_stationCode_stationMetric_stationMetricYear PRIMARY KEY (stationCode, stationMetric, stationMetricYear),
 CONSTRAINT fk_StationMetrics_stationCode FOREIGN KEY (stationCode)
 REFERENCES [Amtrak.Station](stationCode)
  ON DELETE CASCADE ON UPDATE CASCADE
);

--StateMetrics Table
CREATE TABLE [Amtrak.StateMetrics] (
 stateCode CHAR(2) NOT NULL,
 stateMetric VARCHAR(20) NOT NULL,
 stateMetricYear INT NOT NULL,
 stateMetricValue FLOAT,
 CONSTRAINT pk_StateMetrics_stateCode_stateMetric_stateMetricYear PRIMARY KEY (stateCode, stateMetric, stateMetricYear),
 CONSTRAINT fk_StateMetrics_stateCode FOREIGN KEY (stateCode)
 REFERENCES [Amtrak.State](stateCode)
  ON DELETE CASCADE ON UPDATE CASCADE
);

--Route Table
CREATE TABLE [Amtrak.Route] (
 routeID INT NOT NULL,
 routeName VARCHAR(50) NOT NULL,
 routeType VARCHAR(50),
 CONSTRAINT pk_Route_routeID PRIMARY KEY (routeID)
);

--OnTimePerformance Table
CREATE TABLE [Amtrak.OnTimePerformance] (
 routeID INT NOT NULL,
 stationCode CHAR(3) NOT NULL,
 otpYear INT NOT NULL,
 otpValue FLOAT,
 CONSTRAINT pk_OnTimePerformance_routeID_stationCode_otpYear PRIMARY KEY (routeID, stationCode, otpYear),
 CONSTRAINT fk_OnTimePerformance_routeID FOREIGN KEY (routeID)
 REFERENCES [Amtrak.Route](routeID)
  ON DELETE CASCADE ON UPDATE CASCADE,
 CONSTRAINT fk_OnTimePerformance_stationCode FOREIGN KEY (stationCode)
 REFERENCES [Amtrak.Station](stationCode)
  ON DELETE CASCADE ON UPDATE CASCADE
);

--Budget Table
CREATE TABLE [Amtrak.Budget](
 budgetYearID INT NOT NULL,
 budgetType VARCHAR(20) NOT NULL, 
 budgetPlanYear INT NOT NULL,
 budgetTotal FLOAT,
 CONSTRAINT pk_Budget_budgetYearID PRIMARY KEY (budgetYearID)
);

--AllocatedBudget Table
CREATE TABLE [Amtrak.AllocatedBudget](
 stationCode CHAR(3) NOT NULL,
 budgetYearID INT NOT NULL,
 allocatedBudgetYear INT NOT NULL,
 allocatedBudget FLOAT,
 CONSTRAINT pk_AllocatedBudget_stationCode_budgetYearID_allocatedBudgetYear PRIMARY KEY (stationCode, budgetYearID, allocatedBudgetYear),
 CONSTRAINT fk_AllocatedBudget_stationCode FOREIGN KEY (stationCode)
 REFERENCES [Amtrak.Station](stationCode)
  ON DELETE NO ACTION ON UPDATE NO ACTION,
 CONSTRAINT fk_AllocatedBudget_budgetYearID FOREIGN KEY (budgetYearID)
 REFERENCES [Amtrak.Budget](budgetYearID)
  ON DELETE NO ACTION ON UPDATE NO ACTION
);

